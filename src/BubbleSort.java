public class BubbleSort
{
    public int[] sort(int[] unsorted)
    {
        int[] sorted = new int[unsorted.length];

        return sorted;
    }

    public void print(int[] array)
    {
        System.out.println();
        System.out.println();

        for(int i : array)
        {
            System.out.print("| " + i + " ");
        }
    }
}
